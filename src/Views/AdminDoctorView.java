    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        
            String upnic = txtNIC.getText();
            String availability = "";
            if (rbtn1.isSelected()){
            
            availability = "Weekday";
        }
        else if(rbtn2.isSelected()){
            
            availability = "Weekend";
        }
            String p1 = "UPDATE doctor SET DoctorName=?,Speciality=?,Availability=?,DocPassword=?,DocCPassword=? WHERE DoctorNIC ='"+upnic+"' ";
            pst = con.prepareStatement(p1);
            pst.setString(1, txtName.getText());
            pst.setString(2, (String) boxSpc.getSelectedItem());
            pst.setString(3, availability);
            pst.setString(4, pw.getText());
            pst.setString(5, cpw.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Doctor Updated Successfully");
                
                Reset();
            
        } catch (SQLException ex) {
            Logger.getLogger(DrugsView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

