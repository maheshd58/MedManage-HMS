    private void txtNICKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNICKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
              try{
               String getId = txtNIC.getText();
               String q2 = "SELECT * from doctor WHERE DoctorNIC=?";
               pst = con.prepareStatement(q2);
               pst.setString(1, getId);
               rs = pst.executeQuery();
               
               if(rs.next()== false){
                 JOptionPane.showMessageDialog(this, "Doctor Id not found!","Message",JOptionPane.ERROR_MESSAGE);  
        
               }
               else{
                   String dname = rs.getString("DoctorName");
                   txtName.setText(dname.trim());
                   
                   String Spc = rs.getString("Speciality");
                   boxSpc.setSelectedItem(Spc.trim());
                   
                   String avail = rs.getString("Availability");
                   if(avail.equals("Weekday")){
                       rbtn1.setSelected(true);
                   }
        
                   else if(avail.equals("Weekend")){
                       rbtn2.setSelected(true);
                   }
                   
                   String pwd = rs.getString("DocPassword");
                   pw.setText(pwd.trim());
                   
                   String cpwd = rs.getString("DocCPassword");
                   cpw.setText(cpwd.trim());
  
               }
              
               
               
           }catch(Exception e){
               
           }
        }
    }//GEN-LAST:event_txtNICKeyPressed

